掴”伤害到0到10，但仍允许对任何人使用，使用标签“* 0:10”。

用户管理命令:
ulx adduser <用户> <组>-将指定的已连接玩家添加到指定的组。
该组必须存在，此命令才能成功。使用操作员、管理员、超级管理员或see ulx
添加组。您只能指定一个组。参见上文关于免疫的解释。
例1。ulx adduser“Someguy”超级管理员-这将把连接的“some guy”添加为超级管理员
例2。ulx adduser“Dood”monkey-这将把连接的“Dood”添加到组monkey
在该组存在的情况下

ulx removeuser <用户>-从永久访问列表中删除指定的已连接播放器。
例1。ulx removeuser "Foo bar" -这将删除用户的" Foo bar "

ulx userallow <用户> <访问>[<访问标记>] -将访问权限放在用户的允许列表中，使用
