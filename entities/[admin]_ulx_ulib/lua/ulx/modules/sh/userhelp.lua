一般用户管理概念:
用户访问由ULib的Ulysses控制列表(UCL)驱动。此列表包含用户和组
它又包含允许和拒绝访问的列表。允许和拒绝列表包含
访问类似“ulx slap”或“ulx phygunplayer”的字符串，以显示用户和/或组做了什么和做了什么
无权接触。如果用户在其用户允许列表或允许列表中有“ulx slap”
在他们所属的组中，他们可以访问slap。如果用户在其用户拒绝中有“ulx slap”
列出他们被拒绝的命令，即使他们有命令在他们的组的允许之一
列表。这样，deny优先于allow。

ULib通过能够指定允许各种用户和组做什么来支持免疫
目标。这通常是为了让低级别管理员无法针对高级管理员。默认情况下
管理员不能以超级管理员为目标，但是超级管理员仍然可以以管理员为目标。


更高级的概念:
群体有继承性。您可以在addgroup命令中指定它们从哪个组继承。如果a
用户在具有继承的组中，UCL将检查继承中连接的所有组
链条。注意，为了简单起见，组不支持拒绝列表。如果你觉得
团体需要被拒绝一些东西，你应该把你的团体分开。

“用户”组适用于不属于某个组的所有人。你可以用
groupallow在这个组上就像在其他组上一样，只要记住每个人都被允许访问。

ULib通过使用“访问标记”来支持一个高级的、高度可配置的权限系统。接近
标签指定允许用户将什么作为参数传递给命令。例如，你可以做到
所以管理员只能杀死名字中带有“killme”的用户，或者你也可以
允许每个人使用“ulx传送”命令，但只允许他们传送自己。

